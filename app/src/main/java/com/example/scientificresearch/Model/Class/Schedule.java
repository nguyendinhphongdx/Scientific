package com.example.scientificresearch.Model.Class;

public class Schedule {
    private String day;
    private Long startTime;
    private Long endTime;

    public String getDay() { return day; }
    public void setDay(String value) { this.day = value; }

    public Long getStartTime() { return startTime; }
    public void setStartTime(Long value) { this.startTime = value; }

    public Long getEndTime() { return endTime; }
    public void setEndTime(Long value) { this.endTime = value; }
}
